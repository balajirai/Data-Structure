#include <iostream>
using namespace std;

// The standard namespace is a built-in namespace that includes all the entities of the C++ standard library.
// The standard namespace is denoted by the keyword std.

int main() {

    int a = 3, b = 7;
    
    // 'cout' and 'endl' are part of the std namespace
    cout << "Sum: " << a + b << endl;

    return 0;
}
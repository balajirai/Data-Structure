// .max_size() function
// Returns the maximum number of elements that the vector can hold.
#include <bits/stdc++.h>
using namespace std;

int main(){
	// initialize a vector
	vector<int> vec1;

	// returns the max_size of vector
	cout << "max_size of vector 1 = " << vec1.max_size() << endl;

	vector<char> vec2;

	// returns the max_size of vector
	cout << "max_size of vector 2 = " << vec2.max_size() << endl;
	return 0;
}